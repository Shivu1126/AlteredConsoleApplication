package model;

public class TransectionDetail {
		private int accountNumber;
		private String transectionThrough;
		private String method;
		private int amount;
		private int accountBalance;

		public int getAccountNumber() {
			return accountNumber;
		}
		public void setAccountNumber(int accountNumber) {
			this.accountNumber = accountNumber;
		}
		public String getTransectionThrough() {
			return transectionThrough;
		}
		public void setTransectionThrough(String transectionThrough) {
			this.transectionThrough = transectionThrough;
		}
		public String getMethod() {
			return method;
		}
		public void setMethod(String method) {
			this.method = method;
		}
		public int getAmount() {
			return amount;
		}
		public void setAmount(int amount) {
			this.amount = amount;
		}
		public int getAccountBalance() {
			return accountBalance;
		}
		public void setAccountBalance(int accountBalance) {
			this.accountBalance = accountBalance;
		}	
	
}
